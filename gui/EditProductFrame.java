    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
                int isValidInput = validateInput();
        if(isValidInput == 0) {
            JOptionPane.showMessageDialog(null, "Error!Please Fill All the Fields", "ERROR",JOptionPane.ERROR_MESSAGE);
            return;
                    
        }
        else if(isValidInput == 2) {
            return ;
        }
        Product p = new Product();
        p.setProdId((String) cmbProdId.getSelectedItem());
        String catId= (String) categories.get( cmbCatName.getSelectedItem());
        p.setCatId(catId);
        p.setProdName(txtProductName.getText());
        p.setProdprice(Double.parseDouble(txtProductPrice.getText()));
         if(chkIsActive.isSelected() == false)
            p.setIsActive("N");
        else 
            p.setIsActive("Y");
          try {
            boolean chk = ProductDao.updateProduct(p);
            if(chk == true) {
                JOptionPane.showMessageDialog(null, "Product Updated SuccessFully","SUCCESS!",JOptionPane.INFORMATION_MESSAGE);
                
            }
            else
                 JOptionPane.showMessageDialog(null, "Product is not Updated","ERROR!",JOptionPane.INFORMATION_MESSAGE);
        }   catch (SQLException ex) {
               JOptionPane.showMessageDialog(null,"Error In Updating Product","ERROR!",JOptionPane.INFORMATION_MESSAGE);
               ex.printStackTrace();
            }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

