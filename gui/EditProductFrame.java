     public void loadProductDetails() {
         cmbCatName.removeAllItems();
        try {
            categories = CategoryDao.getAllCategory();
            
            for(String catName : categories.keySet()) {
                cmbCatName.addItem(catName);
                
            }
        }   catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error in Connecting DB", "ERROR!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
   }
      private int validateInput() {
       
       String productName = txtProductName.getText();
       String price = txtProductPrice.getText();
       char[] prod_price = price.toCharArray();
        if(productName.isEmpty() || price.isEmpty()) 
           return 0;
       for(char c : prod_price) {
           if(Character.isAlphabetic(c)) {
               JOptionPane.showMessageDialog(null, "Error! Product Price Should have only Numeric values","ERROR!",JOptionPane.ERROR_MESSAGE);
               return 2;
           }
       }
       char [] chars = productName.toCharArray();
       for(char c : chars) {
           if(Character.isDigit(c)) {
               JOptionPane.showMessageDialog(null, "Error! Product Name Should have only characters","ERROR!",JOptionPane.ERROR_MESSAGE);
               return 2;
           }
       }   
           return 1;
    } 


